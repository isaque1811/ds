n1 = int(input("digite o numero do dia de 1 a 7"))
if n1 == 1:
    print("É domingo")
elif n1 == 2:
    print("É segunda")
elif n1 == 3:
    print("É terça")
elif n1 == 4:
    print("É quarta")
elif n1 == 5:
    print("É quinta")
elif n1 == 6:
    print("É sexta")
elif n1 == 7:
    print("É sabado")
else:
    print("Número invalido")